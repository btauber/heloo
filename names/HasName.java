package names;

public class HasName {
    private String name;
    private String Address;
    
    public void setName(String name)
    {
        this.name = name;
    }
    
    public String getName()
    {
        return name;
    } 
    
    public void setAddress(String Address)
    {
        this.Address = Address;
    }
    
    public String getAddress()
    {
        return Address;
    }
    
}
